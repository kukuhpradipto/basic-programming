public class Perulangan {

    public static void main(String[] args) {

        int x = 10;
        for (int i=1; i<=x;i++){
            System.out.println("Suci Cantik Kalimat ke -" + i);
        }

        for (int j = 10; j >= 1; j--){
            System.out.println(j);
        }

        int nilai = 1;
        while(nilai <=10){
            System.out.println(nilai);
            nilai++;
        }
    }

}

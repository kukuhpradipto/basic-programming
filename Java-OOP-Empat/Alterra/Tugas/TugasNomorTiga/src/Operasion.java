public class Operasion {

    public int Penjumlahan (int a, int b){
        int hasil = a + b;
        return hasil;
    }

    public  int Pengurangan (int c, int d){
        int hasil = c - d;
        return hasil;
    }


    public int Perkalian (int e, int f){
        int hasil = e * f;
        return hasil;
    }

    public  int Pembagian (int g, int h){
        int hasil = g / h;
        return hasil;
    }

}

